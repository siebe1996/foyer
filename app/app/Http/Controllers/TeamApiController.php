     *         description="Unauthenticated",
     *     ),
     * )
     */
    public function index()
    {
        $id = Auth::id();
        $teams = Team::where('player1_id', '!=', $id)->where('player2_id', '!=', $id)->get()->map(function ($team){
            return ['id' => $team->id, 'name' => $team->name];
        });
        return response()->json(['data' => $teams]);
    }

